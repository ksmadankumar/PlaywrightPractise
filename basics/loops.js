// for loop, while loop, do while loop

// for to print numbers from 1 to 5
for (let i = 1; i <= 5; i++) {
    console.log(i);
}   
// print hello 5 times using while loop
let count = 1;  
while (count <= 5) {
    console.log("Hello");
    count++;
}
// print hello 5 times using do while loop
let count1 = 1;
do {
    console.log("Hello");
    count1++;
} while (count1 <= 5);
// print hello 5 times using for loop
for (let i = 1; i <= 5; i++) {
    console.log("Hello");
}
